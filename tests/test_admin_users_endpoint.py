def test_default_balance_creates_user_with_grid_key(client, sqlite_engine):
    res = client.post("/admin/users", headers=_auth_headers(), json={})
    assert res.status_code == 201
    body = res.get_json()
    assert body["success"] is True
    app_key = body["data"]["app_key"]
    assert app_key.startswith("grid_")
    assert len(app_key) == len("grid_") + 22

    with sqlite_engine.connect() as conn:
        row = conn.execute(
            text(
                "SELECT balance_points, nickname, mobile, email, company_name "
                "FROM users WHERE app_key = :k"
            ),
            {"k": app_key},
        ).mappings().first()
    assert row is not None
    assert Decimal(str(row["balance_points"])) == Decimal("1000")
    assert row["nickname"] is None
    assert row["mobile"] is None
    assert row["email"] is None
    assert row["company_name"] is None


