# ---------- refresh-after-update race (security hardening) ----------


def test_patch_refresh_race_returns_404(client, sqlite_engine, monkeypatch):
    """If the row vanishes between the UPDATE commit and the refresh
    SELECT, the route must return a clean 404 envelope, not a 500 from
    serializing `None`."""
    app_key = _create_user(client, balance="1000", nickname="Demo User")

    import server

    # First call returns the row (before-lookup); subsequent calls
    # return None to simulate the row being deleted concurrently.
    real_get = server.admin_get_user_by_app_key
    calls = {"n": 0}

    def flaky_get(engine, key):
        calls["n"] += 1
        if calls["n"] == 1:
            return real_get(engine, key)
        return None

    monkeypatch.setattr(server, "admin_get_user_by_app_key", flaky_get)
    res = client.patch(
        f"/admin/users/{app_key}",
        headers=_auth_headers(),
        json={"nickname": "New"},
    )
    assert res.status_code == 404
    assert res.get_json()["error"]["code"] == "USER_NOT_FOUND"
