# ---------- audit logging ----------


def _audit_record(caplog, event: str):
    """Return the most recent LogRecord whose `event` extra matches.

    `caplog.records` carries the original LogRecord objects (not the
    formatted strings), so we can introspect the `extra=` kwargs the
    route passed in."""
    for rec in reversed(caplog.records):
        if getattr(rec, "event", None) == event:
            return rec
    raise AssertionError(
        f"no log record with event={event!r}; saw events: "
        f"{[getattr(r, 'event', None) for r in caplog.records]}"
    )


def test_create_emits_audit_log_with_actor_ip(client, caplog):
    caplog.set_level(logging.INFO, logger="server")
    headers = {**_auth_headers(), "X-Admin-Operator-IP": "203.0.113.7"}
    res = client.post(
        "/admin/users",
        headers=headers,
        json={
            "balance": "1500",
            "nickname": "Demo User",
            "email": "user@example.com",
            "mobile": "13912345678",
        },
    )
    assert res.status_code == 201
    app_key = res.get_json()["data"]["app_key"]

    rec = _audit_record(caplog, "admin.users.create")
    assert rec.actor_ip == "203.0.113.7"
    assert rec.app_key == app_key
    # Balance is recorded verbatim (financial signal, not PII).
    assert rec.balance == "1500"
    # Profile fields are recorded as names only — never raw values.
    assert set(rec.profile_fields_set) == {"nickname", "email", "mobile"}


def test_create_audit_log_does_not_contain_pii_values(client, caplog):
    """Regression coverage (security hardening): mobile / email / nickname /
    company_name values must NOT appear anywhere in the audit log."""
    caplog.set_level(logging.INFO, logger="server")
    res = client.post(
        "/admin/users",
        headers=_auth_headers(),
        json={
            "nickname": "DemoPersonalName",
            "mobile": "13912345678",
            "email": "user@private.example",
            "company_name": "AcmeCo",
        },
    )
    assert res.status_code == 201
    rec = _audit_record(caplog, "admin.users.create")
    rec_repr = repr(vars(rec))
    for pii in ("DemoPersonalName", "13912345678", "user@private.example", "AcmeCo"):
        assert pii not in rec_repr, f"audit log leaks PII: {pii!r}"


