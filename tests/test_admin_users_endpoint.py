"""Integration tests for /admin/users routes.

Covers POST (create, regression coverage) and GET / PATCH /admin/users/<app_key>
(lookup + edit, regression coverage). Same in-memory sqlite + monkeypatched DB engine
pattern as test_account_me_endpoint.py.
"""

from __future__ import annotations

import logging
import sqlite3
from decimal import Decimal

import pytest
from sqlalchemy import create_engine, text


sqlite3.register_adapter(Decimal, str)


AUTH_TOKEN = "test-bff-token"

# Mirrors the post-20260526_0002 schema, with one divergence from
# test_account_me_endpoint.py: `id` is nullable here because production databases use
# `GENERATED BY DEFAULT AS IDENTITY` (auto-fills on INSERT) and sqlite
# has no equivalent default. The route under test intentionally omits
# `id` from its INSERT, so the NOT NULL would fail before any trigger
# could fill it. UNIQUE is retained.
SQLITE_SCHEMA = """
CREATE TABLE users (
    app_key TEXT PRIMARY KEY,
    id INTEGER UNIQUE,
    balance_points NUMERIC NOT NULL DEFAULT 1000 CHECK (balance_points >= 0),
    nickname TEXT,
    mobile TEXT,
    email TEXT,
    company_name TEXT,
    created_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP
);
"""


@pytest.fixture()
def sqlite_engine():
    engine = create_engine(
        "sqlite:///:memory:", connect_args={"check_same_thread": False}
    )
    with engine.begin() as conn:
        conn.execute(text(SQLITE_SCHEMA))
    yield engine
    engine.dispose()


@pytest.fixture()
def client(sqlite_engine, monkeypatch):
    import server

    monkeypatch.setenv("PRICING_BFF_AUTH_TOKEN", AUTH_TOKEN)
    monkeypatch.setattr(server, "get_db_engine", lambda: sqlite_engine)
    monkeypatch.setattr(
        server, "get_db_core_connection", lambda: sqlite_engine.connect()
    )
    return server.app.test_client()


def _auth_headers() -> dict[str, str]:
    return {"Authorization": f"Bearer {AUTH_TOKEN}"}


