    # Balance diff carries verbatim values (financial signal).
    assert rec.balance_before in {"1000", "1000.00"}
    assert rec.balance_after == "5000"
    # Profile change is recorded as a name list, not raw values.
    assert rec.profile_changed == ["nickname"]


