def test_patch_emits_audit_log_with_diff_and_ip(client, caplog):
    app_key = _create_user(client, balance="1000", nickname="Old", email="a@example.com")
    caplog.clear()
    caplog.set_level(logging.INFO, logger="server")
    headers = {**_auth_headers(), "X-Admin-Operator-IP": "198.51.100.42"}
    res = client.patch(
        f"/admin/users/{app_key}",
        headers=headers,
        json={"balance": "5000", "nickname": "New"},
    )
    assert res.status_code == 200

    rec = _audit_record(caplog, "admin.users.update")
